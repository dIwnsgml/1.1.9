import turtle as trtl
import random
p = trtl.Turtle()
p.speed(1000)
def create_ocean():

  p.pensize(1)
  p.begin_fill()
  p.fillcolor("light blue")
  p.penup()
  p.goto(-400,0)
  p.pendown()
  p.pencolor("light blue")
  while p.xcor() < 400:
    p.sety(0)
    p.setheading(0)
    p.pendown()
    p.circle(29,60)
    p.setheading(300)
    p.circle(29,60)
  p.goto(400, -300)
  p.goto(-400,-300)
  p.goto(-400,0)
  p.end_fill()

  p.begin_fill()
  p.fillcolor("#3B5387")
  p.penup()
  p.goto(-400,-25)
  p.pendown()
  p.pencolor("azure")
  p.pensize(4)
  while p.xcor() < 400:
    p.sety(-25)
    p.setheading(0)
    p.pendown()
    p.circle(29,60)
    p.setheading(300)
    p.circle(29,60)
  p.goto(400, -300)
  p.goto(-400,-300)
  p.goto(-400,0)
  p.end_fill()

  p.begin_fill()
  p.fillcolor("#1F254A")
  p.penup()
  p.goto(-400,-50)
  p.pendown()
  p.pencolor("azure")
  p.pensize(4)
  while p.xcor() < 400:
    p.sety(-50)
    p.setheading(0)
    p.pendown()
    p.circle(29,60)
    p.setheading(300)
    p.circle(29,60)
  p.goto(400, -300)
  p.goto(-400,-300)
  p.goto(-400,0)
  p.end_fill()

  #this code for sand
  p.pensize(1)
  p.penup()
  p.goto(-400,-270)
  p.pendown()
  p.begin_fill()
  p.pencolor("black")
  p.fillcolor("#ADAD8B")
  p.setheading(30)
  p.circle(-200,30)
  p.circle(-200,30)
  p.goto(400, -300)
  p.goto(-400,-300)
  p.goto(-400,0)
  p.end_fill()

  #this code for seeweed
  def seaweed(x, y):
    p.penup()
    for i in range(3):
      p.penup()
      p.goto(x, y)
      p.pendown()
      p.begin_fill()
      p.pencolor("black")
      p.fillcolor("#1A5704")
      p.setheading(53)
      p.circle(15,70)
      p.left(110)
      p.circle(15, 70)
      y += 15
      p.end_fill()
  def rock(x, y):
    p.penup()
    p.goto(x, y)
    p.pendown()
    p.pencolor('black')
    p.fillcolor('darkgrey')
    p.begin_fill()
    p.setheading(0)
    p.circle(10,360,5)
    p.end_fill()
    
  for i in range(10):
    seaweed(random.randrange(-400,400), -300)
    rock(random.randrange(-400, 400), -300)